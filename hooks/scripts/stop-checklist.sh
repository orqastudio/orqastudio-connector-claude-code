#!/usr/bin/env bash
# Stop hook — writes session state and provides pre-commit checklist
#
# Session state is written to tmp/session-state.md so the next session
# can pick up exactly where this one left off.

set -euo pipefail

PROJECT_DIR="${CLAUDE_PROJECT_DIR:-.}"
OUTPUT=""

# ─── Session State ────────────────────────────────────────────────────────────
# Write structured session state for the next session to pick up.
# The agent should have written this already via the orchestrator protocol,
# but we gather what we can from git state as a safety net.

SESSION_FILE="$PROJECT_DIR/tmp/session-state.md"
mkdir -p "$PROJECT_DIR/tmp"

# Find active epics and tasks from .orqa/
ACTIVE_EPICS=""
if [ -d "$PROJECT_DIR/.orqa/delivery/epics" ]; then
  ACTIVE_EPICS=$(grep -l "^status: active" "$PROJECT_DIR"/.orqa/delivery/epics/*.md 2>/dev/null | while read f; do
    id=$(grep "^id:" "$f" | head -1 | sed 's/^id:\s*//' | tr -d '"')
    title=$(grep "^title:" "$f" | head -1 | sed 's/^title:\s*//' | tr -d '"')
    echo "- $id: $title"
  done || true)
fi

ACTIVE_TASKS=""
if [ -d "$PROJECT_DIR/.orqa/delivery/tasks" ]; then
  ACTIVE_TASKS=$(grep -l "^status: active" "$PROJECT_DIR"/.orqa/delivery/tasks/*.md 2>/dev/null | while read f; do
    id=$(grep "^id:" "$f" | head -1 | sed 's/^id:\s*//' | tr -d '"')
    title=$(grep "^title:" "$f" | head -1 | sed 's/^title:\s*//' | tr -d '"')
    echo "- $id: $title"
  done || true)
fi

# Get recent commits from this session (last 5)
RECENT_COMMITS=$(cd "$PROJECT_DIR" && git log --oneline -5 2>/dev/null || true)

# Get uncommitted changes summary
UNCOMMITTED_FILES=$(cd "$PROJECT_DIR" && git status --short 2>/dev/null || true)
UNCOMMITTED_COUNT=$(echo "$UNCOMMITTED_FILES" | grep -c '.' 2>/dev/null || echo "0")

# Write session state (only if the agent hasn't written a richer one)
# Check if the file was written recently (within last 60 seconds) by the agent
WRITE_STATE=true
if [ -f "$SESSION_FILE" ]; then
  # If the file contains orchestrator-maintained content (step checklists,
  # architecture decisions), never overwrite — regardless of age.
  # The orchestrator updates session state in real time (RULE-4f7e2a91).
  if grep -q "^### Steps\|^### Completed\|^- \[x\]\|^- \[ \]" "$SESSION_FILE" 2>/dev/null; then
    WRITE_STATE=false
  # Fallback: if modified in the last 60 seconds, preserve it
  elif find "$SESSION_FILE" -mmin -1 2>/dev/null | grep -q .; then
    WRITE_STATE=false
  fi
fi

if [ "$WRITE_STATE" = true ]; then
  cat > "$SESSION_FILE" << SESSIONEOF
## Session: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

### Active Epics
${ACTIVE_EPICS:-No active epics found}

### Active Tasks
${ACTIVE_TASKS:-No active tasks found}

### Recent Commits
${RECENT_COMMITS:-No commits in this session}

### Uncommitted Changes
${UNCOMMITTED_COUNT} files with uncommitted changes

### Notes
Session state auto-generated by stop hook. If the orchestrator wrote a more detailed state, this file was preserved.
SESSIONEOF
fi

# ─── Auto-fix Pass ────────────────────────────────────────────────────────────
# Run orqa validate --fix to auto-remediate fixable integrity issues.
# If fixes are applied, stage them so they are included in the next commit.

ORQA_FIX_OUTPUT=""
ORQA_FIX_ERRORS=0
ORQA_FIXES_APPLIED=0

if command -v orqa >/dev/null 2>&1; then
  ORQA_FIX_RESULT=$(orqa validate --fix "$PROJECT_DIR" 2>&1 || true)

  # Detect whether fixes were applied (the binary prints a "fixes_applied" JSON key)
  if echo "$ORQA_FIX_RESULT" | grep -q '"fixes_applied"'; then
    # Count how many fixes were applied
    ORQA_FIXES_APPLIED=$(echo "$ORQA_FIX_RESULT" | python3 -c "
import json, sys
try:
    data = json.load(sys.stdin)
    print(len(data.get('fixes_applied') or []))
except Exception:
    print(0)
" 2>/dev/null || echo "0")

    if [ "$ORQA_FIXES_APPLIED" -gt 0 ]; then
      # Stage auto-fixed .orqa/ files
      (cd "$PROJECT_DIR" && git add ".orqa/" 2>/dev/null || true)
      ORQA_FIX_OUTPUT="orqa validate --fix: applied ${ORQA_FIXES_APPLIED} auto-fix(es) — changes staged"
    fi
  fi

  # Check for remaining errors after fix
  REMAINING_ERRORS=$(echo "$ORQA_FIX_RESULT" | python3 -c "
import json, sys
try:
    data = json.load(sys.stdin)
    checks = data.get('checks') or []
    print(sum(1 for c in checks if c.get('severity') == 'Error'))
except Exception:
    print(0)
" 2>/dev/null || echo "0")

  if [ "$REMAINING_ERRORS" -gt 0 ]; then
    ORQA_FIX_ERRORS=$REMAINING_ERRORS
    OUTPUT="${OUTPUT}VALIDATION ERRORS: ${REMAINING_ERRORS} integrity error(s) remain after auto-fix. Fix before committing.\n\n"
  fi
fi

# ─── Pre-Stop Output ──────────────────────────────────────────────────────────

# Check for open worktrees
MAIN_DIR=$(cd "$PROJECT_DIR" && git rev-parse --show-toplevel 2>/dev/null || echo "$PROJECT_DIR")
WORKTREES=$(cd "$PROJECT_DIR" && git worktree list 2>/dev/null | grep -v "$MAIN_DIR" || true)
if [ -n "$WORKTREES" ]; then
  OUTPUT="${OUTPUT}WARNING: Open worktrees detected — merge and clean up before reporting task complete:\n${WORKTREES}\n\n"
fi

if [ "$UNCOMMITTED_COUNT" -gt 0 ]; then
  OUTPUT="${OUTPUT}UNCOMMITTED CHANGES: ${UNCOMMITTED_COUNT} files. Commit or stash before ending session.\n\n"
fi

# ─── Epic Completion Check ────────────────────────────────────────────────────
# Check if the scoped epic has incomplete tasks. If so, remind the orchestrator
# that the epic isn't done — RULE-046 (continuous operation) applies.

EPIC_REMINDER=""
if command -v orqa >/dev/null 2>&1; then
  # Find the scoped epic from session state (look for EPIC- references)
  SCOPED_EPIC=""
  if [ -f "$SESSION_FILE" ]; then
    SCOPED_EPIC=$(grep -oP 'EPIC-[a-f0-9]{8}' "$SESSION_FILE" 2>/dev/null | head -1 || true)
  fi

  if [ -n "$SCOPED_EPIC" ]; then
    # Query the graph for tasks delivering to this epic
    EPIC_TASKS=$(orqa graph --type task 2>/dev/null | grep -i "$SCOPED_EPIC" || true)
    INCOMPLETE=$(cd "$PROJECT_DIR" && grep -rl "delivers" .orqa/delivery/tasks/*.md 2>/dev/null | while read f; do
      if grep -q "$SCOPED_EPIC" "$f" 2>/dev/null; then
        status=$(grep "^status:" "$f" | head -1 | sed 's/^status:\s*//' | tr -d '"')
        if [ "$status" != "completed" ] && [ "$status" != "surpassed" ] && [ "$status" != "archived" ]; then
          title=$(grep "^title:" "$f" | head -1 | sed 's/^title:\s*//' | tr -d '"')
          echo "  - $status: $title"
        fi
      fi
    done || true)

    if [ -n "$INCOMPLETE" ]; then
      EPIC_REMINDER="EPIC NOT COMPLETE — $SCOPED_EPIC has incomplete tasks (RULE-046):\n${INCOMPLETE}\n\nRESUME WORKING NOW. Do not stop. Do not wait for the next session. Continue with the next incomplete task immediately.\n\nPRIORITY: Lesson promotions (recurrence >= 2) and rule violations are CRITICAL — always address before delivery tasks. The learning loop breaks if governance debt accumulates.\n\n"
    else
      EPIC_REMINDER="EPIC COMPLETE — $SCOPED_EPIC: all tasks completed. Summarise the work done before ending.\n\n"
    fi
  fi
fi

if [ -n "$EPIC_REMINDER" ]; then
  echo -e "$EPIC_REMINDER"
fi

cat <<'EOF'
SESSION END PROTOCOL:

Before stopping, the orchestrator MUST:
1. Run `orqa validate --fix` to auto-fix missing inverses and verify graph integrity
2. Write session state to tmp/session-state.md:
   - Scope: which epic/task was in focus
   - Done: what was completed this session
   - In Progress: what's partially done
   - Next: what should happen next session
   - Blockers: anything blocking progress
3. Commit all work
4. Verify checks pass (make check)

PRE-STOP CHECKLIST:
- orqa validate --fix passes (0 errors)
- All checks pass (make check)
- No stub/mock/placeholder patterns in committed code
- Changes are within task scope
- No TODO comments or stubs added
- Documentation updated if behaviour changed
- Session state written to tmp/session-state.md

FORBIDDEN:
- NEVER use --no-verify — fix the errors instead
- NEVER skip pre-commit hooks
- NEVER end a session with uncommitted changes
EOF

if [ -n "$ORQA_FIX_OUTPUT" ]; then
  echo ""
  echo "$ORQA_FIX_OUTPUT"
fi

if [ -n "$OUTPUT" ]; then
  echo ""
  echo -e "$OUTPUT"
fi

exit 0
